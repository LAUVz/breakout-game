import Brick from './brick.js';

export function buildLevel (game, level) {
    let bricks = [];

    level.forEach((row, rowIndex) => {
        row.forEach((brick,  brickIndex) => {
            let position = {
                x: 8 + 72 * brickIndex,
                y: 70 + 32 * rowIndex
            };

            if (brick > 0) {
                bricks.push(new Brick(game, position, rowIndex, brick));
            }
        });
    });

    return bricks;
}

